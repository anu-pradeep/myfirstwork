void main()
{
  int a= 10;
  int b= 5;
  print("a+b= ${a+b}");
  print("a-b= ${a-b}");
  print("a*b= ${a*b}");
  print("a/b= ${a/b}");
  print("a~/b= ${a~/b}"); // ~/ null division for avoid point values eg:23.02 by using ~/ answer is 23
  print("a%b= ${a%b}");  // % modulus operator used for finding reminder in division
  print("-(a+b)= ${-(a+b)}"); // unary minus for getting reverse sign of the expression
}